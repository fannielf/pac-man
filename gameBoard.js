export const width = layout.length;
export const squares = [];
const grid = document.querySelector('.grid');

function createBoard() {
    for (let row = 0; row < layout.length; row++) {
        for (let col = 0; col < layout[row].length; col++) {
            const square = document.createElement('div');
            grid.appendChild(square);
            squares.push(square);

            // add layout to the board using row and col
            const tile = layout[row][col];
            if (tile === 0) {
                squares[squares.length - 1].classList.add('pac-dot');
            } else if (tile === 1) {
                squares[squares.length - 1].classList.add('wall');
            } else if (tile === 2) {
                squares[squares.length - 1].classList.add('ghost-lair');
            } else if (tile === 3) {
                squares[squares.length - 1].classList.add('power-pellet');
            }
        }
    }
}

createBoard();
